#include "libft.h"
#include <stdlib.h>

void	ft_lstclear(t_list **lst, void (*del)(void *))
{
	t_list	*tmp;

	if (!lst || !del)
		return ;
	tmp = *lst;
	while (tmp)
	{
		(*del)(tmp->content);
		tmp = tmp->next;
		free(*lst);
		*lst = tmp;
	}
}
